

#include <iostream>
#include <string>


std::string decodeURL(const std::string& url)
{
	std::string decoded;
	
	decoded.reserve(url.size());







	return (decoded);
}

int main(void)
{
	{
		std::cout << "original:\n";

		try
		{

			std::cout << "decoded:\n";
		}
		catch(const std::exception& e)
		{
			std::cerr << e.what() << '\n';
		}

	}
}
